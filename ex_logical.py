# And operator
print("==> And operator <==")
print("x and y :", x and y)
print("x and z :", x and z)
print("z and y :", z and y)
print("", end=("\n" * 2))

# Or Operator
print("==> Or operator <==")
print("x or y :", x or y)
print("x or z :", x or z)
print("z or y :", z or y)
print("", end=("\n" * 2))

# Not Operator
print("==> Not operator <==")
print("not x :",not x)
print("not y :", not y)
print("not z :", not z)
print("", end=("\n" * 2))
