# Lebih besar dari
print("<== Lebih besar dari ==>")
print("x > y :", x > y)
print("t > s :", t > s)
print("", end=("\n" * 2))


# Lebih kecil dari
print("<== Lebih kecil dari ==>")
print("x < y :", x < y)
print("t < s :", t < s)
print("", end=("\n" * 2))


# Sama dengan
print("<== Sama dengan ==>")
print("z == x :", z == x)
print("t == u :", t == u)
print("", end=("\n" * 2))


# Tidak sama dengan
print("<== Tidak sama dengan ==>")
print("z != x :", z != x)
print("t != u :", t != u)
print("", end=("\n" * 2))


# Lebih besar dari atau sama dengan
print("<== Lebih besar dari atau sama dengan ==>")
print("z >= x :", z >= x)
print("u >= t :", u >= t)
print("s >= z :", s >= z)
print("", end=("\n" * 2))


# Lebih kecil dari atau sama dengan
